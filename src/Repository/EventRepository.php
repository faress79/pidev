    public function searchBynom($value): array
    {
        return $this->createQueryBuilder('o')
            
        ->select('o.id','o.nom_event','o.adress_event','o.description','o.image','o.date_event') // Select only the nom_event column
        ->andWhere('o.nom_event LIKE :val')
        ->setParameter('val', '%' . $value . '%')
        ->orderBy('o.id', 'ASC')
        ->setMaxResults(10)
        ->getQuery()
        ->getResult();
    }
